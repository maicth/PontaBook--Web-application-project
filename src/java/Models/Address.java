package Models;

/**
 *
 * @author DELL
 */
public class Address {
    private int id;
    private int userId;
    private String name;
    private String add;
    private String phone;

    public Address() {
    }

    public Address(int id, int userId, String name, String add, String phone) {
        this.id = id;
        this.userId = userId;
        this.name = name;
        this.add = add;
        this.phone = phone;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAdd() {
        return add;
    }

    public void setAdd(String add) {
        this.add = add;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
